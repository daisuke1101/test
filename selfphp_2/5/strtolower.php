<?php
print strtolower( 'WINGSプロジェクト' ) . '<br />'; // 大文字をすべて小文字に変換
print strtoupper( 'wingsプロジェクト' ) . '<br />'; // 小文字をすべて大文字に変換
print lcfirst( 'WINGS PROJECT' ) . '<br />';    // 文字列の１文字目を小文字に変換
print ucfirst( 'wings project' ) . '<br />';    // 文字列の１文字目を大文字に変換
print ucwords( 'wings project' ) . '<br />';    // 単語の区切りをすべて大文字に変換